//Exercício de loop com for para exibir na tela do cmd os números de 1 a 100. Sem acento para rodar no cmd. Feito no Bloco de Notas.

public class LoopFor {

	public static void main (String[] args) {
	
		for (int i = 1;i <= 100; i++){ //o contador i pode ser iniciado fora do for, se fizer isso, não precisa declarar o tipo de novo.
		System.out.println(i + "\n");
		}
	}
}